     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
    	$request->validate([
            'body'=>'required',
        ]);
        $input = $request->all();

        $input['user_id'] = auth()->user()->id;
        $input['body'] = $request->body;

        Comment::create($input);
        $input['type'] = "comment";
        $input['body'] = $request->body;
        Notification::create($input);
        return back();

    }

